#!/usr/bin/env python3
# Basic System Monitoring Script
# Generated by Implementation Kickstart

import psutil
import time
import json
from datetime import datetime
from pathlib import Path

def collect_system_metrics():
    """Collect basic system metrics"""
    return {
        'timestamp': datetime.now().isoformat(),
        'cpu_percent': psutil.cpu_percent(interval=1),
        'memory_percent': psutil.virtual_memory().percent,
        'disk_usage': psutil.disk_usage('/').percent,
        'load_average': psutil.getloadavg() if hasattr(psutil, 'getloadavg') else None
    }

def check_resource_thresholds(metrics):
    """Check if metrics exceed warning thresholds"""
    warnings = []
    
    if metrics['cpu_percent'] > 80:
        warnings.append(f"HIGH CPU: {metrics['cpu_percent']:.1f}%")
    
    if metrics['memory_percent'] > 85:
        warnings.append(f"HIGH MEMORY: {metrics['memory_percent']:.1f}%")
    
    if metrics['disk_usage'] > 90:
        warnings.append(f"HIGH DISK: {metrics['disk_usage']:.1f}%")
    
    return warnings

def main():
    print("üìä BASIC SYSTEM MONITORING")
    print("=" * 40)
    
    # Collect metrics
    metrics = collect_system_metrics()
    
    # Display current status
    print(f"‚è∞ Timestamp: {metrics['timestamp']}")
    print(f"üñ•Ô∏è  CPU Usage: {metrics['cpu_percent']:.1f}%")
    print(f"üß† Memory Usage: {metrics['memory_percent']:.1f}%")
    print(f"üíæ Disk Usage: {metrics['disk_usage']:.1f}%")
    
    # Check for warnings
    warnings = check_resource_thresholds(metrics)
    if warnings:
        print(f"\n‚ö†Ô∏è  WARNINGS DETECTED:")
        for warning in warnings:
